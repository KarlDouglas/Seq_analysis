BC1_mutations = main.calculate_mutations(BC1, main.count_depth("BC1.map"))
BC1_dist = main.plot_mutation_distribution(BC1_mutations)
plt.savefig("BC1.dist.png")
BC1_substitutions = main.plot_substitution_base_proberbility(BC1)
plt.savefig("BC1.substitutions.png")
BC1_mutations = main.plot_mutation_base_proberbility(BC1)
plt.savefig("BC1.mutations.png")
print("Sequence depth BC1:", (main.count_depth("BC1.map")))
print("Nucleotides counted, Mutations counted, mutation frequency",main.total_mutations(BC1))

